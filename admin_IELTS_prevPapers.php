<h2>Speaking (11&ndash;14 minutes)</h2>
<p>In the Speaking test, you have a discussion with a certified examiner. It is interactive and as close to a real-life situation as a test can get.<br>
<br>
There are three parts to the test and each part fulfils a specific function in terms of interaction pattern, task input and test taker output.<br>
<br>
In Part 1, you answer questions about yourself and your family. In Part 2, you speak about a topic. In Part 3, you have a longer discussion on the topic.<br>
<br>
The Speaking test is the same for both Academic and General Training versions. Each of the three parts is designed to test a different aspect of your communication ability.</p>

<h2>Academic Writing (60 minutes)</h2>
<p>The Academic Writing tests consist of two writing tasks of 150 words and 250 words.<br />
<br />
In Task 1, test takers are asked to describe some visual information (graph/table/chart/diagram). They need to write 150 words in about 20 minutes. In Task 2 test takers are presented with a point of view or argument or problem. They need to write their response in 250 words in about 40 minutes.<br />
<br />
Below are samples of Task 1 and Task 2. <br />
<br />
Please note that test takers for IELTS Academic take a different Writing test in IELTS General Training.</p>

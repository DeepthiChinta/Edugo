    <h2>Online Test Instructions</h2>
    <ul>
    <li>To attempt 160 questions, you&#8217;ll have 180 minutes.</li>
    <li>There will be no deductions for incorrect responses.</li>
    <li>Only objective-type questions appear on the Question Paper.</li>
    <li>English and Telugu versions of the questions will be provided.</li>
    <li>In both languages, the questions will appear one at a time on the screen.</li>
    <li>Each question will have four possibilities, one of which will be the correct answer, and the candidate will be required to choose one.</li>

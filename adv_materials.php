        <h1>About JEE Advanced 2023</h1>
        <p> JEE Advanced is the only entrance exam that opens the gates of IIT admissions in undergraduate courses leading to a bachelor's degree, Integrated master's degree, Bachelor-Master Dual Degree in Engineering, Science, or Architecture. Joint Entrance Examination â€“ Advanced is a national-level exam and is the second level that the candidates need to qualify to be part of IIT alumni, the first being JEE Main. Every year around the top 2.5 lakh JEE Main candidates are allowed to seat for this entrance test.  The exam is conducted by any one of the 7 IITs namely IIT Kharagpur, IIT Kanpur, IIT Madras, IIT Delhi, IIT Bombay, IIT Guwahati, and IIT Roorkee or Indian Institute of Science (IISc) Bangalore, under the aegis of the Joint Admission Board (JAB).</p>
        
        <p>
            JEE Advanced qualifying candidates are then offered seats in IITs based on their course preference and JEE Advanced ranks via a common process by the Joint Seat Allocation Authority (JoSAA). The courses offered by the IITs via JEE Advanced 2023 are BTech, BS, BArch, BTech and MTech dual degree, BS and MS dual degree, Integrated MTech, and Integrated MSc.</p>

  <h1>Exam Pattern</h1>
  <ul>
    <strong>JEE Advanced</strong>
  </ul>
  <ul>
    <li>JEE Advanced is a computer-based test.</li>
    <li>The exam consists of two papers - Paper 1 and Paper 2, both of which are mandatory.</li>
    <li>Each paper has a duration of 3 hours.</li>
    <li>Both papers are divided into three sections - Physics, Chemistry, and Mathematics.</li>
    <li>Each section consists of both Multiple Choice Questions (MCQs) and Numerical Value Answer (NVA) type questions.</li>
    <li>Negative marking is applicable for MCQs. A wrong answer will result in a negative score of -1 or -2 marks depending on the question pattern.</li>
    <li>The questions are designed to test the understanding, application, and analytical skills of the candidates.</li>
    <li>The syllabus for JEE Advanced includes topics from Class 11 and 12 of Physics, Chemistry, and Mathematics.</li>
  </ul>

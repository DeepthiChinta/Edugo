   <h2>Whatâ€™s New in CAT Exam Pattern?</h2> 
   <p>There was no change in CAT 2022 exam pattern. The <strong>CAT exam pattern</strong> was revised in 02021 when IIM Ahmedabad, the exam conducting institute of CAT for that year reduced the number of questions from 76 to 66.</p> 

       <h2>CAT Application Process: Errors to Avoid</h2> 
       <p>CAT application form can be edited only before final submission. The CAT form correction window gives opportunity to edit only a few fields, so it is better to fill the form error free in the first attempt. Check below a few points to <strong>avoid errors in CAT form 2023:</strong></p> 
       <ul> 
        <li>Use a <strong>functional email ID and mobile phone number</strong> as all the communication from CAT registration to IIM shortlist will be done on email and mobile.</li> 
        <li><strong>Select CAT test city carefully</strong> as once the exam centre is allotted, no request for change will be entertained.</li> 
        <li>Choose your current city as first preference for allotment of exam centre. Keep in mind your convenience of reaching the test centre on the exam day, before selecting six cities in order of 1-6.</li> 
        <li><strong>Select IIMs carefully</strong> as you will be eligible for WAT-PI of only those IIMs which you shall have selected in the CAT application form.</li> 
        <li><strong>Select IIM interview cities also carefully.</strong> Only two options are available for each IIM so make selection carefully.</li> 
        <li><strong>Enter accurate academic scores</strong> for class 10, 12, Graduation. Also, make sure you furnish information of PG or professional degree as the IIMs will refer to CAT application forms for candidate’s academic record for WAT-PI shortlisting.</li> 
        <li>Enter <strong>work experience details</strong> carefully too, as it will be analysed by the IIMs for shortlisting students for WAT-PI.</li> 
        <li><strong>Upload valid caste/PwD/Scribe certificates</strong> along with the CAT application form. If the certificates turn out to be fake, candidates may be barred from appearing in the exam.</li> 
        <li><strong>Ensure fee payment is successful</strong> and you receive the acknowledgement in email. If the fee payment is not successful, the form submission will remain incomplete and CAT admit card will not be issued.</li> 
       </ul> 

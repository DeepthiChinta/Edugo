      <p><span style="color: #e03e2d;"><span style="color: #000000;"><strong>CAT 2023 form correction</strong> window will open for four to five days after the registration process has concluded.</span></span> Candidates will be able to upload a better photograph and image of their signature in the CAT form or change the test city preference while the form correction window is open.</p> 
      <ul> 
       <li>Log on to CAT 2023 website</li> 
       <li>Click on ‘login’ tab</li> 
       <li>Enter User ID and password</li> 
       <li>Click on Application Form</li> 
       <li>Go to relevant section (photo, signature, test city) and make changes</li> 
       <li>Make changes and enter CAPTCHA code and click on continue to complete the process</li> 
      </ul>
      


      <h2>What is CAT Eligibility Criteria 2023?</h2>  
      <ul> 
       <ul> 
        <li>Eligibility criteria for CAT exam is graduation with minimum 50 per cent aggregate (45 per cent for SC/ST/PwD candidates) or equivalent degree from a recognised university</li> 
        <li>Final year graduation students are also eligible to appear for CAT but the average score of first two years must be 50 per cent (45 per cent for SC/ST/PwD candidates)</li> 
        <li>Candidates of professional courses such as ICAI, ICSI,ICWAI are also eligible to apply for CAT exam</li> 
       </ul> 

      <h2><strong style="font-size: 1rem;">CAT Registration cum Application Process </strong><strong style="font-size: 1rem;">2023 </strong></h2>  
      <p>CAT exam registration is a <strong>three-step process</strong> which includes online registration, filling up the CAT form and payment of fees. Check below the <strong>step-by-step process of CAT 2023 registration</strong>: </p> 
      <h3><strong>Step 1: Register for CAT 2023</strong></h3> 
      <p>To apply for CAT 2023, candidates have to register on the official website of IIMCAT. Follow the steps given below for online registration:</p> 
      <ul> 
       <li>Open the official CAT website <strong>iimcat.ac.in</strong></li> 
       <li>Click on 'Register' tab under 'New Candidate Registration' tab</li> 
       <li>Enter name, date of birth, email ID, mobile phone number and nationality</li> 
       <li>Click on 'Generate OTP' tab</li> 
       <li>On successful registration, an OTP will be sent on the registered mobile number  </li> 
       <li>Verify CAT 2023 registration by entering the OTP in the given field </li> 
      </ul> 
       
      <p><strong>The CAT form has six sections. </strong>Candidates are required to fill relevant details in all the six pages of the form for successful submission. Check below details related to the six sections of CAT exam registration form 2023:</p> 
      <ol> 
       <li><strong>Personal Details</strong>: In this section of CAT application form, the candidates are required to enter details such as name, date of birth, father’s name, category, gender, nationality, communication address, email ID, mobile phone number, etc.</li> 
       <li><strong>Academic Details:</strong> In this section, the candidates have to enter class 10 and 12 exam scores, bachelor’s degree details and marksheet and details of other professional courses (if any).</li> 
       <li><strong>Work Experience:</strong> If a candidate has relevant work-experience, they need to fill in details such as name of the company, duration, etc., in this section.</li> 
       <li><strong>Programmes:</strong> In this section, the candidate has to select IIM institutes of their choice and the MBA programmes they wish to pursue.</li> 
       <li><strong>Test City:</strong> In this section, test city preference is to be marked. Candidates can select up to six cities in order of preference from first to last in the CAT application form.</li> 
       <li><strong>Payment:</strong> To pay the CAT application fee, candidates can use Credit/Debit Card, Internet Banking or Paytm/PayU mobile wallets.</li> 
      </ol> 
      <p><strong>Step 3: Payment of CAT Registration cum Application Fee</strong></p> 
      <p>The third and last step in CAT exam registration 2023 is payment of application fee. After furnishing details in the first five sections, click on 'save and continue' to proceed to fee payment window.</p> 

      <h3><strong>CAT 2023 Application Process: Selection of Test Cities</strong></h3> 
      <p>At the time of filling the <strong>CAT form</strong>, candidates can mention preferred test location by selecting up to six options from the list of 158 cities. However, allotment of the CAT test city will be subject to availability of seats at the centre. </p> 
      <h2><strong>CAT Exam Registration 2023: Things to Keep in Mind</strong></h2> 
      <ul> 
       <li>The <strong>preferred web browsers</strong> to fill CAT 2023 application form are <strong>Edge Chromium or Mozilla Firefox (version 56 and above) and Google Chrome (version 59 and above) </strong></li> 
       <li>Do not fill the CAT application form on a smart phone. Use a laptop or a desktop for filling the application form.</li> 
       <li><strong>Cross-check</strong> all the information filled in the <strong>CAT application form</strong> thoroughly before hitting ‘submit/save key’.</li> 
       <li>Once submitted, the form cannot be edited. You will have to create a new login ID, fill the form and pay the fee again.</li> 
       <li>Save a copy of CAT application fee acknowledgment slip and a copy of duly filled application form for future reference.</li> 
       <li>Preserve the CAT registration confirmation mail until the end of MBA admission process. It is advisable to take a printout of the confirmation mail.</li> 
      </ul> 
      <h3>CAT Registrations 2023: Instructions for Reserved Category Students</h3> 
      <ul> 
       <li>Candidates who wish to apply under Reserved category must strictly follow the instructions mentioned specifically for such candidates.</li> 
       <li>To check Caste/Tribe name applicable to you, check the National Commission for Backward Classes (NCBC) website: http://www.ncbc.nic.in/Home.aspx.</li> 
       <li>The CAT 2023 application form will also have this information. Click the icon on the form to open the National Commission for Backward Classes (NCBC) website.</li> 
       <li>Submission of Resaved category form is mandatory. Download the EWS, NC-OBC, SC/ST Certificate from the CAT 2022 official website, fill and self-attest, scan as PDF document and upload on the CAT website.</li> 
       <li>If you are <strong>applying under Person with Disability (PwD)</strong> category, download the Person with Disability (PwD) Certificate from the CAT website, enter the required information and self-attest, and scan as PDF document to upload the certificates on the CAT website.</li> 
       <li>Do not forget to select details under PwD Category - Wheelchair Support and Scribe Requirement, in the CAT application form 2023.</li> 
       <li>If you will be taking assistance of a Scribe for appearing in the exam, fill the <strong>Scribe Affidavit form available</strong> on the CAT exam registration website before the deadline and arrange a scribe yourself.</li> 
      </ul> 

<h2>AP EAPCET Eligibility Criteria 2023 for Engineering Course</h2>
<p>The eligibility criteria for the Engineering course are given below:</p>
<table><caption>AP EAPCET Eligibility Criteria 2023 for Engineering Course</caption>
<thead>
<tr>
<td>
<p><strong>Criteria</strong></p>
</td>
<td>
<p><strong>Features</strong></p>
</td>
</tr>
</thead>
<tbody>
<tr>
<td>
<p><strong>Age Criteria</strong></p>
</td>
<td>The minimum age limit is 17 years of age.There is no upper age limit.</td>
</tr>
<tr>
<td>
<p><strong>Educational Qualification</strong></p>
</td>
<td>
<ul>
<li aria-level="1">Applicants must have passed class 12 from the recognised board.</li>
<li aria-level="1">Candidates appearing for the class 12 exam can also apply for AP EAPCET 2023.</li>
<li aria-level="1">Candidates who completed Diploma in Engineering course are also eligible to apply.</li>
</ul>
</td>
</tr>
<tr>
<td>
<p><strong>Marks Required</strong></p>
</td>
<td>
<ul>
<li aria-level="1">Candidates must have obtained a minimum of 45% aggregate in the class 12 exam for the general category and 40% aggregate for reserved category candidates.</li>
</ul>
</td>
</tr>
<tr>
<td>
<p><strong>Nationality</strong></p>
</td>
<td>
<ul>
<li aria-level="1">Candidate must be an Indian citizen or belong to a Person of Indian origin.</li>
<li aria-level="1">Candidates must fulfil the local/non-local status requirements of the Andhra Pradesh State Government.</li>
<li aria-level="1">The candidate must be a domicile of the Andhra Pradesh state and possess a domicile certificate</li>
</ul>
</td>
</tr>
</tbody>
</table>

<h2strong>How to prepare for CAT 2023?</strong></h2> 
<p>CAT preparation based on the official syllabus is the only way to score high in the exam. Candidates interested in appearing for CAT exam must make a preparation plan based on their knowledge of the&nbsp;<strong>&nbsp;topics included in the CAT syllabus</strong>. Many Quantitative Aptitude topics are those, which are taught in school and so is the case with English Grammar. So, the first thing to do to prepare for CAT is make the list of topics in terms of familiarity –&nbsp;&nbsp;<strong>familiar, somewhat familiar&nbsp;</strong>and&nbsp;<strong>not familiar at all</strong>. After that, start studying unfamiliar topics first and then move to somewhat familiar and finally to familiar topics. Take a look at some&nbsp;<strong>section-wise tips for CAT preparation</strong>&nbsp;are listed below:&nbsp;</p> 
<h3><strong>How to Prepare for CAT VARC?</strong></h3> 
<ul> 
 <li>VARC preparation should start at the early stage of CAT preparation, specifically Reading Comprehension practice</li> 
 <li>Check the VARC syllabus of CAT and revise all the Grammar rules and definitions</li> 
 <li>Get hold of a dictionary and word meaning books to learn new words and phrases</li> 
 <li>RC dominates VARC section so it is important have the practice of speed-reading. This can be developed by reading books, novels, magazines and data heavy reports.</li> 
 <li>The best source of preparation of Verbal Ability is sample questions. Solve a large number and a wide range of Verbal Ability questions to master it.</li> 
</ul> 
<h3><strong>How to Prepare for CAT DILR?</strong></h3> 
<ul> 
 <li>DILR section of CAT is all about Tables, Graphs, Charts and Series</li> 
 <li>This section requires, good calculation skill as well as eye for detail</li> 
 <li>24 questions of this section will be equally divided into DI and LR thus, you need to work on both</li> 
 <li>Memorise Tables up to 20, Square, Square Roots, Cube, Cube Roots and other such mathematical values to read and calculate fast</li> 
 <li>Practice as many sample questions and sectional mock tests as you can because there is no formula to be remembered</li> 
 <li>The more experience you have at speed reading charts and tables better the chances of scoring high in DILR section of CAT</li> 
</ul>

<h3><strong>CAT Syllabus: Verbal Ability Key Points</strong></h3> 
<ul> 
 <li>In CAT exam, there are usually eight Verbal Ability questions</li> 
 <li>The&nbsp;<strong>questions in Verbal Ability </strong>section are&nbsp;<strong>non-MCQs</strong>, which means candidates have to Type In The Answer (TITA)</li> 
 <li>There is&nbsp;<strong>no negative marking in Verbal Ability </strong>since the questions are non-MCQ type</li> 
 <li>Types of questions in CAT VA include: 
  <ul> 
   <li>Para-jumbles</li> 
   <li>Out of context sentence in a paragraph</li> 
   <li>Error correction in a paragraph</li> 
   <li>Vocabulary usage</li> 
   <li>Sentence completion</li> 
   <li>Analogies</li> 
   <li>Critical reasoning</li> 
  </ul></li> 
</ul> 
<h3><strong>CAT Syllabus: Reading Comprehension Key Points</strong></h3> 
<ul> 
 <li>Reading Comprehension questions are all MCQ type, hence there is&nbsp;<strong>negative marking of one mark</strong></li> 
 <li>On an average, RC section of the CAT exam has&nbsp;<strong>three to four long passages and two to three short passages</strong></li> 
 <li>In CAT 2022, there were only four passages. In CAT 2023 too, candidates can expect&nbsp;<strong>four RC passages</strong></li> 
 <li>The long passages in CAT are of&nbsp;<strong>900 words</strong>and short passages are of&nbsp;<strong>600 words&nbsp;</strong></li> 
 <li>The RCs could be from&nbsp;<strong>Science, History, Politics, Environment, Society to Literature, Mythology and Abstract topics</strong></li> 
</ul> 

       <h1> Exam Pattern </h1>

      <p> The Test of English as a Foreign Language (TOEFL) is a standardized test that measures the English language ability of non-native speakers. The test is commonly required for admission to universities in English-speaking countries. There are two versions of the TOEFL test: the TOEFL iBT (Internet-based test) and the TOEFL PBT (paper-based test). </p>

      <p>  The TOEFL iBT consists of four sections: reading, listening, speaking, and writing. The reading section consists of three or four passages and a total of 36-70 questions. The listening section consists of six to nine passages and a total of 34-51 questions. The speaking section consists of six tasks and the writing section consists of two tasks. The test is approximately four hours long. </p>

      <p> The TOEFL PBT consists of three sections: listening comprehension, structure and written expression, and reading comprehension. The listening comprehension section consists of 50 questions and the structure and written expression section consists of 40 questions. The reading comprehension section consists of 50 questions. The test is approximately three hours long. </p>


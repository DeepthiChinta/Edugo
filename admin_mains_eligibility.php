    <h1>Eligiblity</h1>

    <strong>Educational Background</strong>
    <p>The candidate must be either appearing or must have cleared the 10+2 level exam or equivalent from an authorized university or board to be eligible for JEE Main 2023. The 10+2 qualifying year could be 2023 or 2022 and those appearing in the qualifying exam in 2023 are also eligible to apply.</p>

    <p>Students who hold a diploma are also eligible. But the exception for the diploma holders will be that they will only be considered for the IITs. The scorecard will not be applicable for NITs and CFTIs.</p>
    
    <strong>Age Limit</strong>
    <p>There is no age limit for the JEE Main. Those candidates who have cleared the 10+2 or equivalent exam in 2022,2023 or are going to appear in 2024, irrespective of their age are eligible to apply for the JEE Main 2023 exam.</p>

    <strong>Year of Passing Qualifying Exam</strong>
    <p>The students who have passed their 12th board examination in the year 2022, 2023 and who are going to appear for 10+2 in 2023 are eligible to apply.</p>

    <strong>Subjects in the Qualifying exam</strong>
    <p>As per the specific subject criterion, the candidates must have passed Mathematics, Physics along with Chemistry, Biology, Biotechnology, or any other technical vocational subject from the 5 subjects opted. Those who have qualified the exam with 4 subjects are not eligible to apply.</p>


